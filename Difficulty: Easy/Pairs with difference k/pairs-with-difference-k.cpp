class Solution {
  public:
    /* Returns count of pairs with difference k  */
    int countPairsWithDiffK(vector<int>& arr, int k) {
        // code here
        map<int,int>map;
        for(int i = 0; i < arr.size(); i++){
            map[arr[i]]++;
        }
        int c = 0;
        for(int i = 0; i < arr.size(); i++){
            if(map.find(k+arr[i])!=map.end()){
                c += map[arr[i]+k];
                // map[arr[i]+k] = 0;
            }
        }
        return c;
    }
};

